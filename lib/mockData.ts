export function getPropertyById(id: string): Property | undefined {
  return mockProperties.find((p) => p.id === id);
}

export function filterProperties(params: {
  type?: string;
  minPrice?: number;
  maxPrice?: number;
  beds?: number;
  baths?: number;
  waterfront?: boolean;
  pool?: boolean;
  neighborhood?: string;
  newConstruction?: boolean;
}): Property[] {
  return mockProperties.filter((p) => {
    if (params.type && params.type !== 'all' && p.property_type !== params.type) return false;
    if (params.minPrice && p.price < params.minPrice) return false;
    if (params.maxPrice && p.price > params.maxPrice) return false;
    if (params.beds && p.beds < params.beds) return false;
    if (params.baths && p.baths < params.baths) return false;
    if (params.waterfront === true && !p.is_waterfront) return false;
    if (params.pool === true && !p.has_pool) return false;
    if (params.neighborhood && p.neighborhood !== params.neighborhood) return false;
    if (params.newConstruction === true && p.property_type !== 'new-construction') return false;
    return true;
  });
}
